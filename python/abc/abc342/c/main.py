    for i in range(97, 123):
        group[chr(i)] = chr(i)

    for i in range(q):
        c, d = s_map()
        change_l = []
        for k, v in group.items():
            if v == c:
                change_l.append(k)

        for cl in change_l:
            group[cl] = d

    result = [''] * n
    for k, v in group.items():
        for i in range(n):
            if s[i] == k:
                result[i] = v
    # for k, v in group.items():
